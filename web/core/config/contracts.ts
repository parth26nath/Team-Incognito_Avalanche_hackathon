// Helper function to get contract address for current chain
export const getContractAddress = (chainId: number, contract: 'HEARTLY' | 'USDC'): string => {
  return CONTRACT_ADDRESSES[chainId]?.[contract] || "";
};

// Validation function
export const validateContractAddresses = (chainId: number): boolean => {
  const addresses = CONTRACT_ADDRESSES[chainId];
  return addresses && addresses.HEARTLY !== "" && addresses.USDC !== "";
};
